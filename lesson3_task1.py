name = "Olha"
day = "Thursday"
# f-string
print(f"Good day {name}! {day} is a perfect day to learn some python.")
# .format()
print("Good day {}! {} is a perfect day to learn some python.".format(name, day))
# Concatenation
print("Good day " + name + "! " + day + " is a perfect day to learn some python.")
